package Emplo_Java;

public class Ejemplo_Loop_DoWhile {

	public static void main(String[] args) {
	
		int b = 13;
			 do {
				 // este codigo dentro del Do se imprime incluso si la condicion del while es falsa
				 System.out.println("El valor de B es: " + b);
				 b++;
			 }while (b <= 20);

	}

}
