package Emplo_Java;

public class Ejemplo_If_Switch {

	public static void main(String[] args) {
		
		int i = 10;
		
		switch (i) {
		
		case 0:
			System.out.println("i es Cero");
			break;
		case 1:
			System.out.println("i es uno");
			break;
		case 2:
			System.out.println("i es dos");
			break;
		default:
			System.out.println("i es mayor que 2");
			break;

		
		}

	}

}
